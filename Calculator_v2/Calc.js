class Calc {
    add (num1, num2) {
        return num1 + num2;
    }

    substract (num1, num2) {
        return num1 - num2;
    }

    multiply (num1, num2) {
        return num1 * num2;
    }
    
    divide (num1, num2) {
        return num1 / num2;
    }

    percent (num1) {
        return num1/100;
    }

    opposite (num1) {
        return -(num1);
    }
}